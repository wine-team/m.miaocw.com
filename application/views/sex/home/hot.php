        startIndex:0,
        autoScroll:false,
        swipendfn:null,
        callback:function(f){
			cgnav(f.index);
        }
});

function cgnav(id){
	
	var im=$bx.eq(id);
	im.show().siblings().hide();
	if(dian[id]==1){
		lazynav(id);
		window.scrollTo(0,vtop);
		dian[id]=0;
	}
}

function lazynav(id){
	var imgs=$bx.eq(id).find("img");
	imgs.each(function(){
		var a=$(this).attr("src3");
		$(this).attr("src",a).removeAttr("src3");	
	});
}
window.onscroll=function(){
	
	var sTop=document.documentElement.scrollTop+document.body.scrollTop;
	if(sTop>=vtop){
		if (isf==0) {
			hbar.addClass("fix");
			isf=1;
		}
	}else{
		if(isf==1){
			hbar.removeClass("fix");	
			isf=0;
		}
	}
}
